# Quick Recap

# variables
lecture_day = 2
print(lecture_day)

# numbers can be int, float or complex
square_area = 36
circle_area = 6.28
iota = (-1) ** (1/2)
print(square_area, circle_area, iota)

# basic mathematical operators
print(square_area + circle_area)
print(4 * square_area)
print(iota/square_area)

# type function
print(type(iota))

# strings
greeting = "Hello from Scaler!"
print(greeting)

# f-strings
msg = "Hello from"
name = "Scaler"
print(f"{msg} {name}!")

# input from user
num1 = int(input())
num2 = int(input())
print(num1 + num2)

# none value
x = None
print(x)